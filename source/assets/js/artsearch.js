var artsearch = document.getElementById("artsearch");

	

	artsearch.addEventListener("click", function(){ 	
	var show = document.getElementById("artsearchfield").style.display;
	    	if(show === "none"){
	    		document.getElementById("artsearchfield").style.display = "inline";
	    	}else { 
	    		document.getElementById("artsearchfield").style.display = "none";
	    		}
        });







